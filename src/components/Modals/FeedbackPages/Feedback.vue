<script setup>
import { useLoggingStore } from '@/stores/LoggStore';

const loggingStore = useLoggingStore();

function submit() {
    const results = {};
    const questionElements = document.querySelectorAll('.question');

    questionElements.forEach((question, index) => {
        const questionName = `question${index + 1}`;
        const selectedOption = question.querySelector(`input[name="${questionName}"]:checked`);
        results[questionName] = selectedOption ? selectedOption.value : null;
    });

    loggingStore.submitFeedback(results);
}
